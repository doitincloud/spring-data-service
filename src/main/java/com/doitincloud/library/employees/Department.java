package  com.doitincloud.library.employees;

import com.fasterxml.jackson.annotation.JsonInclude;
import com.fasterxml.jackson.annotation.JsonPropertyOrder;

@JsonInclude(JsonInclude.Include.NON_NULL)
@JsonPropertyOrder({ "deptNo", "deptName" })
public class Department {

    private String deptNo;

    private String deptName;

    public Department() {
    }

    public Department(String deptNo, String deptName) {
        this.deptNo = deptNo;
        this.deptName = deptName;
    }

    public String getDeptNo() {
        return deptNo;
    }

    public void setDeptNo(String deptNo) {
        this.deptNo = deptNo;
    }

    public String getDeptName() {
        return deptName;
    }

    public void setDeptName(String deptName) {
        this.deptName = deptName;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (!(o instanceof Department)) return false;

        Department that = (Department) o;
        if (!getDeptNo().equals(that.getDeptNo())) return false;
        return getDeptName().equals(that.getDeptName());
    }

    @Override
    public int hashCode() {
        int result = getDeptNo().hashCode();
        result = 31 * result + getDeptName().hashCode();
        return result;
    }
}
